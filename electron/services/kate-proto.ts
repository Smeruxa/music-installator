export interface GcmAuthData {
    id: string
    token: string
    idStr?: Buffer
}

export function getMTalkRequest(authData: GcmAuthData): Buffer {
    const idBuf = Buffer.from(authData.id, 'utf8')
    const tokenBuf = Buffer.from(authData.token, 'utf8')
    const hexId = `android-${(authData.idStr ?? Buffer.from(authData.id, 'utf8')).toString('hex')}`
    const hexIdBuf = Buffer.from(hexId, 'utf8')

    const msg = Buffer.concat([
        Buffer.from(
            '\x0a\x0a\x61\x6e\x64\x72\x6f\x69\x64\x2d\x31\x39\x12\x0f\x6d\x63\x73\x2e\x61\x6e\x64\x72\x6f\x69\x64\x2e\x63\x6f\x6d\x1a',
            'binary'
        ),
        writeVarint(idBuf.length),
        idBuf,
        Buffer.from('\x22', 'binary'),
        writeVarint(idBuf.length),
        idBuf,
        Buffer.from('\x2a', 'binary'),
        writeVarint(tokenBuf.length),
        tokenBuf,
        Buffer.from('\x32', 'binary'),
        writeVarint(hexIdBuf.length),
        hexIdBuf,
        Buffer.from(
            '\x42\x0b\x0a\x06\x6e\x65\x77\x5f\x76\x63\x12\x01\x31\x60\x00\x70\x01\x80\x01\x02\x88\x01\x01',
            'binary'
        )
    ])

    return Buffer.concat([Buffer.from('\x29\x02', 'binary'), writeVarint(msg.length), msg])
}

export function decodeCheckinResponse(msg: Buffer, needIdStr: boolean): GcmAuthData {
    let data = msg
    let idFound = false
    let tokenFound = false
    let id = ''
    let token = ''
    let idStr: Buffer | undefined

    while (data.length > 0) {
        const field = readVarint(data)
        data = field.rest
        const wtype = field.value & 0x7
        const fieldNum = field.value >> 3

        switch (wtype) {
            case 0: {
                const v = readVarint(data)
                data = v.rest
                break
            }
            case 1: {
                if (fieldNum === ID_NUM) {
                    idFound = true
                    const r = read64(data)
                    if (needIdStr) idStr = r.raw
                    id = r.value
                    data = r.rest
                } else if (fieldNum === TOKEN_NUM) {
                    tokenFound = true
                    const r = read64(data)
                    token = r.value
                    data = r.rest
                } else {
                    data = data.subarray(8)
                }
                if (tokenFound && idFound) {
                    return { id, token, idStr }
                }
                break
            }
            case 2: {
                const len = readVarint(data)
                data = len.rest.subarray(len.value)
                break
            }
            default:
                throw new ProtobufError(`Unexpected wire type ${wtype}`)
        }
    }

    throw new ProtobufError('id/token not found in checkin response')
}
