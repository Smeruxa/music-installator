const ID_NUM = 7
const TOKEN_NUM = 8

export class ProtobufError extends Error {
    constructor(message: string) {
        super(message)
        this.name = 'ProtobufError'
    }
}

export function writeVarint(num: number | bigint): Buffer {
    let n = BigInt(num)
    const parts: number[] = []
    while (true) {
        const t = Number(n & 0x7fn)
        n >>= 7n
        if (n !== 0n) {
            parts.push(t | 0x80)
        } else {
            parts.push(t)
            break
        }
    }
    return Buffer.from(parts)
}

function readVarint(data: Buffer): { value: number; rest: Buffer } {
    let num = 0
    let i = 0
    while (true) {
        if (i >= data.length) {
            throw new ProtobufError('Varint not found')
        }
        const byte = data[i]
        if (byte & 0x80) {
            num |= (byte ^ 0x80) << (7 * i)
            i++
        } else {
            num |= byte << (7 * i)
            break
        }
    }
    return { value: num, rest: data.subarray(i + 1) }
}

function read64(data: Buffer): { value: string; rest: Buffer; raw: Buffer } {
    if (data.length < 8) {
        throw new ProtobufError('Int64 not found')
    }
    const raw = data.subarray(0, 8)
    const lo = raw.readUInt32LE(0)
    const hi = raw.readUInt32LE(4)
    const value = (BigInt(hi) << 32n) + BigInt(lo)
    return { value: value.toString(), rest: data.subarray(8), raw: Buffer.from(raw) }
}

